package com.collar.values.var;

public class Varriable_03 {

	public static void main(String[] args) {
		
		// 정수형 변수 num1 선언
		// 정수값을 저장할 기억장소를 예약하고 num1이름을 부여하기
		int num1;
		// 한 번 변수를 선언하는 데 사용한 이름으로 또 다시 변수를 선언할 수 없다
		
		//또다른 기억장소를 예약하고 num2 이름 부여
		int num2;
		
		// 선언된 변수에 값을 저장하기
		num1 = 30;
		num2 = 40;
		
		System.out.println(num1);
		System.out.println(num2);
		
		// num1 변수와 num2 변수에 어떤 값이 저장되어 있는 지 관심X
		// 모두 제거하고 num1에는 150으로 num2에 250을 저장하라
		num1 = 150;
		num2 = 250;

	}

}
